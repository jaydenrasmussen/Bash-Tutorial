#!/usr/bin/env bash
# Sh builtins
ls # shows the current directory contents
pwd # Print Working Directory
cat # outputs the content in a file
head # prints the first line of a file
tail # prints the last line of the file
exit # exits the shell
ps # lists the running processes
which # lists the location of the executable to be ran
man # manual for a command
unset # delete variables or functions
kill # end a process
wait # wait for a process to exit, then display its value
# Print commands 
echo
printf
# Bash builtins
nano/vim/emacs # edit the contents of a file (VIM is the best)
alias # shorten common commands with these
unalias # delete your aliases
# these two are dangerous, they enable and disable builtin commands
enable
disable
logout # logs out of the shell
